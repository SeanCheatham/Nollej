	<p>
        Nollej is an engine that constructs a semantic web of Wikipedia.  Wikis consists of text and information, but they also contain embedded links to other wikis.  This linkage of wiki topics is similar in functionality to the human brain.
    </p>
    <p>Nollej is written in PHP and utilizes the CodeIgniter framework.  It also uses an open source Wikipedia interface library and the ArborJS graphing library.</p>
